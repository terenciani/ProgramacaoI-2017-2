package br.estacio.programacao.metodos;

/**
 *
 * @author Marcelo
 */
public class CalculadoraNaoEstatica {
    public int somar(int a, int b){
        return a + b;
    }
    public int multiplicar(int a, int b){
        return a * b;
    }
    public int dividir(int a, int b){
        return a / b;
    }
    public int subtrair(int a, int b){
        return a - b;
    }
    
}
